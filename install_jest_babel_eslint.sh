#!/usr/bin/env bash
# Install Jest, Babel, and ESLint
# in your project directory:

# Install Jest using:
sudo npm install --save-dev jest

# Install Babel using:
sudo npm install --save-dev babel-jest @babel/core @babel/preset-env

# Install ESLint using:
sudo npm install --save-dev eslint

# Then Finally
sudo npm install # to install all necessary project dependencies.
